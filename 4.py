
set1 = {1,2,3,4,5,6,7,8}
set2 = {6,7,4,8,9,10}

union_set = set1.union(set2)
intersection_set = set1.intersection(set2)
print(f"This union is {union_set} and length of this set is {len(union_set)}")
print(f"This intersection is {intersection_set} and length of this set is {len(intersection_set)}")
